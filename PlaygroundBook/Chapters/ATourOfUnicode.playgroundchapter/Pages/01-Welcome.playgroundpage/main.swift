# A Tour of Unicode

## What Is of Unicode?

**Unicode is the world standard for text and emoji.** It is the foundation for text in any languages in all modern software.

Every time we type a key on our phone or desktop computer, or look at a web page or text in an application, we are interacting with Unicode.

## What Does Unicode Include?

Contents included in the Unicode standard are:

* A set of code charts for visual reference
* An encoding method and a set of standard character encodings
* A set of reference data files, and a number of related items, including:
  * Character properties
  * Rules for normalization, decomposition, collation, rendering, and bidirectional text display order (for text mixing both right-to-left scripts, such as Arabic and Hebrew, and left-to-right scripts).

## How Is Unicode Named?

Joe Becker, one of the co-founders of the Unicode project, published a draft proposal for an "international/multilingual text character encoding system" in August 1988. He called it "Unicode" and explained that: *The name 'Unicode' is intended to suggest a **unique, unified, universal** encoding*.

## How Unicode Evolves?

Unicode Consortium is the nonprofit organization that coordinates Unicode's development.

The Unicode Standard Version 1.0 was first published in 1991), and has published new versions on a regular basis since then.

The latest and the single valid version is version 13.0. It was released in March 2020, and has been implemented in all morden operating systems.

